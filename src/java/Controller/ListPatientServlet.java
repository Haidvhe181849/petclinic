    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
            HttpSession session = request.getSession();
        Integer doctorIdObj = (Integer) session.getAttribute("doctorId");

        // Kiểm tra đăng nhập
        if (doctorIdObj == null) {
            response.sendRedirect("../login.jsp");
            return;
        }

        int doctorId = doctorIdObj;

        BookingDAO dao = new BookingDAO();
        List<User> patients = dao.getPatientsByDoctor(doctorId);

        request.setAttribute("patients", patients);
        request.getRequestDispatcher("/doctor/patients.jsp").forward(request, response);
    }
    

